package edu.lfa.df.dto;

/**
 *
 * @author LAKHE
 */
public class RoleDTO {
    public int id;
    public String role;

    public RoleDTO() {
    }

    public RoleDTO(int id, String role) {
        this.id = id;
        this.role = role;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
    
    
}
